package com.risen.entity;

public class CoreDictionary {

	private Integer id;				//ID
	private String corecdKey;		//键
	private String corecdVal;		//值
	private String corecdType;		//字典分类
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCorecdKey() {
		return corecdKey;
	}
	public void setCorecdKey(String corecdKey) {
		this.corecdKey = corecdKey;
	}
	public String getCorecdVal() {
		return corecdVal;
	}
	public void setCorecdVal(String corecdVal) {
		this.corecdVal = corecdVal;
	}
	public String getCorecdType() {
		return corecdType;
	}
	public void setCorecdType(String corecdType) {
		this.corecdType = corecdType;
	}
	
	
}
