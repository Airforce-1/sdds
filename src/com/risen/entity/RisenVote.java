package com.risen.entity;

import java.util.Date;

public class RisenVote {
	private Integer id;
	private Integer voteType;
	private String voteTitle;
	private String voteDes;
	private Date startTime;
	private Date endTime;
	private Boolean status;//是否启用
	private Integer voteNature;//模板1时  题目类型（1单选，2多选）
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getVoteType() {
		return voteType;
	}
	public void setVoteType(Integer voteType) {
		this.voteType = voteType;
	}
	public String getVoteTitle() {
		return voteTitle;
	}
	public void setVoteTitle(String voteTitle) {
		this.voteTitle = voteTitle;
	}
	public String getVoteDes() {
		return voteDes;
	}
	public void setVoteDes(String voteDes) {
		this.voteDes = voteDes;
	}
	public Date getStartTime() {
		return startTime;
	}
	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
	public Boolean getStatus() {
		return status;
	}
	public void setStatus(Boolean status) {
		this.status = status;
	}
	public Integer getVoteNature() {
		return voteNature;
	}
	public void setVoteNature(Integer voteNature) {
		this.voteNature = voteNature;
	}
	
	
}
